		<h1>Como colaborar  no mapasColetivos</h1>
        <p> Existem duas maneiras de participar no mapeamento coletivo, colaborando em um mapa coletivo existente ou
        criando um novo mapeamento e convidando outras pessoas a colaborarem. Para qualquer uma destas formas de colaboração, você precisa estar registrado no mapasColetivos. </p>
        <div class="static_content_area h7">
        <h7>  01.  Criando sua conta</h7>
        </div>

		<p> Crie o seu perfil de mapeador clicando em <strong><a href="http://mapascoletivos.com.br/index.php/users/signup">Cadastrar</a></strong>. Se você já possui nome de usuário e senha, clique em <strong><a href="http://mapascoletivos.com.br/index.php/login">Entrar</a></strong>.</p>

		<div class="static_content_area h7">
        <h7>  02.  Como criar um novo mapa coletivo</h7>
        </div>

		<p> Crie um novo mapa clicando no botão <strong>criar novo mapa</strong>, localizado no canto direito da barra superior das páginas e 
        <br> na sua página de perfil (parte inferior do contador de mapasColetivos). </p>
        
       <p> Siga as instruções do vídeo tutorial para configurar o seu novo mapa.  </p>           
    <iframe width="560" height="315" src="http://www.youtube.com/embed/cEskVe-OA84" frameborder="0" allowfullscreen></iframe>
    
   
   		<div class="static_content_area h7">
        <h7>  03.  Como adicionar informações ao mapa coletivo</h7>
        </div>
        
		 <p>. Na página de edição do mapa, use a <strong> barra de busca </strong> ou a ferramenta <img src="http://mapascoletivos.com.br/media/img/tutorial_ferramenta_pin.png" align="bottom"/><strong> criação de ponto </strong> para criar um novo ponto.</p>
         Clique com o <img src="http://mapascoletivos.com.br/media/img/tutorial_ferramenta_cursor.png" align="bottom"/><strong> cursor </strong> sobre o ponto criado para adicionar o seu relato, fotos e vídeos (link youtube).</p>
         
         <iframe width="560" height="315" src="http://www.youtube.com/embed/xmtoBUtN504" frameborder="0" allowfullscreen></iframe>
         <br></br>

		<p>. Use a ferramenta  <img src="http://mapascoletivos.com.br/media/img/tutorial_ferramenta_import.png" align="bottom"/> <strong> importar camadas</strong> para enviar suas rotas e compartilhar bases de dados com outros mapeadores. </p>
        <p>O mapasColetivos suporta a importação de dados geográficos nos formatos <strong>.kml</strong> e <strong>.kmz</strong>, comumente utilizados nos programas Google Mapas e Google Earth. Se o seu arquivo com ponto ou rota possui outros formatos advindos de aparelhos de GPS ou programas GIS, 
         veja <strong><a href="http://mapascoletivos.com.br/index.php/static/kml">aqui como convertê-lo</a></strong><br> em .kml para poder importá-lo ao mapasColetivos.</p>
