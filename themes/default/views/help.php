		<h1>Para colaborar  no mapasColetivos.com  basta seguir este passo a passo</h1>

		<p>- Crie o seu perfil de mapeador clicando em <strong>Cadastrar.</strong> Se você já possui Nome de usuário e senha, clique em <strong>Entrar</strong></p>

		<p>- <strong>Crie um novo mapa</strong> clicando nos botões na página inicial ou em sua página de perfil </p>

		<p>- Dê um título para o seu mapa e descreva os objetivos dele. Por exemplo "Quero mapear o número de lixeiras na Avenida Paulista"</p>

		<p class="no_margin">- Escolha uma das<strong> 11 categorias</strong>  que representam a qualidade de vida da cidade e que melhor resume o tema do seu mapa. Você também pode criar tags para o seu mapa.</p><p> Isso ajuda outros mapeadores a encontrem seu mapa.  Tente utilizar nomes de bairros, ruas ou temas mais específicos, como pontos de reciclagem, centros culturais, escolas e hospitais.</p>

		<p>-  Determine o <strong>zoom</strong>  de seu mapa usando a ferramenta interativa</p>

		<p>- <strong>Clique em salvar. Pronto o seu mapa está pronto para ser editado.</strong></p> 

		 <p>- Para<strong> marcar um ponto</strong> e adicionar o seu relato, fotos, vídeos e notícias, use as ferramentas indicadas e utilize a caixa de envio de dados.</p>

		<p>- Use a ferramenta Adicionar KML para enviar suas rotas e compartilhar suas bases de dados com outros mapeadores</p>

		<p class="no_margin">- Você tambêm pode organizar seus pontos com legendas dentro do mapa. Exemplo: o mapa Lixeiras na Avenida Paulista, poderia ter legendas como `lixeiras quebradas`, e `esquinas sem lixeiras`.</p><p> Não há limite para legenda
		s mas seu mapa ficará mais legal se tiver legendas que sejam representativas.</p>

		<p>-Quando terminar de incluir os seus pontos, clique em <strong>Finalizar edição</strong>  e você poderá enviar convites para seus amigos colaborarem em seu mapa</p>

		<p>- Os pontos criados em seu mapa farão parte do grande mapa coletivo exibido na página inicial.</p>

		<p>- Para acessar seus pontos, clique em seu nome no topo da página  e volte ao seu perfil. Ali você verá a sua geografia pessoal e poderá edita os relatos enviados</p>

		<p>- Utilizando a <strong>galeria de mapas</strong>, navegue pelos mapas criados pelos outros mapeadores. Você pode seguir os mapas de eu interesse  e colaborar em quantos quiser. </p>
		
	  
		
