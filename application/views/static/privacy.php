	 <p>Termos de Uso do mapasColetivos</p>
      <br>
      <strong><p>Seção 1 : Regras de uso</p> </strong>
   <br>
<p>1.1 Ao criar um mapa em Mapas Coletivos, os usuários cadastrados, sejam eles pessoas físicas ou Jurídicas se comprometem a:</p>
<p>
a) não utilizar a ferramenta para fins comerciais, como propaganda de estabelecimentos eventos com cobrança de ingresso, ou que representem um interesse político ou religioso.
<br>
b) não difundir mensagens de violência, discriminação social, religiosa, racial ou sexual
<br>
c) não utilizar termos pejorativos nos títulos e descrições dos mapas e pontos
<br>
d) respeitar as categoria temáticas disponibilizadas no momento da criação do mapa
</p>
<br>
1. 2.  A equipe do mapasColetivos se reserva o direito de:
<p>
a) apagar mapas, relatos e pontos que transgridam os termos definitos do parágrafo 1.1 deste documento.
<br>
b) excluir usuários que apresentem comportamento abusivo no uso da ferramenta de mapeamento
</p>
<br>
<p>1. 3 . Política de Comentários</p>
<p>A política de comentários de mapasColetivos seguirá as normas estabelecidas pela Associação O Eco, organização executora, conforme documento disponibilizado no seguinte endereço http://www.oeco.com.br/politica-comentarios" </p>
<br>
<strong><p>Seção 2 : Política de Dados</p></strong>
<br>
<p>2.1 Compromisso de mapasColetivos</p>
<br>
<p>a) Os dados dos usuários fornecidos no cadastro não serão compartilhados com empresas de mala direta ou quaisquer outras atividades comerciais, a não ser aquelas que estejam sendo realizadas dentro do plano de apoio à plataforma www.mapascoletivos.com.br<br>
b) Atender a pedidos para exclusão de quaisquer dados que afetem a privacidade de seus usuários e a comunidade.<br>
c) Utillizar os dados apenas com fins jornalísticos, fazendo monitoramento de reutilizações de material anteriormente publicado em nossa plataforma <br>
</p>
<br>
<p>2.2 Compromisso do usuário</p>
<br>
<p>a) Ceder os direitos de uso dos dados de localização dos relatos, fotos, vídeos e links bem como autorizar a reutilização destes dados para análises gráficas não comerciais e veiculação de conteúdo jornalístico em www.mapascoletivos.com.br <br>
b) Utilizar dados que não infrinjam as correntes leis brasileiras civis e criminais e que respeitem direitos autorais de obras audiovisuais e textos que venham a ser utilizados na plataforma www.mapascoletivos.com.br <br>
c) Utilizar a ferramenta de upload de dados apenas para disponibilizar bases de dados que não estejam protegidas por sigilo oficial ou que representem risco de segurança à fonte de informação. </br>
d) Utilizar a ferramenta de download de dados somente com fins não comerciais. </br>
</p>
